type EmojiGroupKey = keyof typeof EMOJI_GROUPS;
const GROUP_ORDER: EmojiGroupKey[] = ["people", "smileys", "animals", "food", "activities", "travel", "objects", "symbols"];

function groupLabel(t: Dictionary, group: EmojiGroupKey): string {
  return t.emojiPicker.groups[group];
}

function EmojiGrid({ value, onChange }: { value: string | null | undefined; onChange: (emoji: string | null) => void }) {
  const t = useI18n();
  const [group, setGroup] = useState<EmojiGroupKey>(
    GROUP_ORDER.find((g) => (EMOJI_GROUPS[g] as readonly string[]).includes(value ?? "")) ?? "travel"
  );

  return (
    <div className="flex flex-col gap-3 pt-2">
      <h2 className="text-[1.1875rem] font-extrabold tracking-[-0.01em]">{t.emojiPicker.title}</h2>
      <p className="text-caption-airbnb text-muted-foreground">{t.emojiPicker.sub}</p>

      <div className="flex gap-1 overflow-x-auto pb-1">
        {GROUP_ORDER.map((g) => (
          <button
            key={g}
            type="button"
            onClick={() => setGroup(g)}
            className={cn(
              "shrink-0 rounded-full px-2.5 py-1.5 text-xs font-semibold whitespace-nowrap",
              group === g ? "bg-primary/10 text-primary" : "bg-surface-0 text-muted-foreground"
            )}
          >
            {groupLabel(t, g)}
          </button>
        ))}
      </div>

      <div className="grid max-h-[45vh] grid-cols-7 gap-1.5 overflow-y-auto">
        {EMOJI_GROUPS[group].map((emoji, i) => (
          <button
            key={`${emoji}-${i}`}
            type="button"
            onClick={() => onChange(emoji)}
            className={cn(
              "flex aspect-square items-center justify-center rounded-control border border-border text-[1.25rem] transition-colors hover:bg-muted",
              value === emoji && "border-primary bg-primary/10"
            )}
          >
            {emoji}
          </button>
        ))}
      </div>

      {value !== null && value !== undefined && (
        <Button type="button" variant="outline" onClick={() => onChange(null)}>
          {t.emojiPicker.clear}
        </Button>
      )}
    </div>
  );
}

/**
 * Controlled bottom sheet for picking the Telegram-summary emoji shown in
 * Zone's header line (see docs at telegram-format.ts). Separate from
 * IconPickerSheet (icon-picker.tsx) — that one picks the SVG icon shown in
 * the owner UI, this one picks a Unicode emoji Telegram can actually render
 * inline in message text.
 */
export function EmojiPickerSheet({
  open,
  onClose,
  value,
  onChange,
}: {
  open: boolean;
  onClose: () => void;
  value: string | null | undefined;
  onChange: (emoji: string | null) => void;
}) {
  return (
    <BottomSheet open={open} onClose={onClose} className="max-h-[80vh]">
      <EmojiGrid
        value={value}
        onChange={(emoji) => {
          onChange(emoji);
          onClose();
        }}
      />
    </BottomSheet>
  );
}
