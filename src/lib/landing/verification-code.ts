// Код подтверждения владения сайтом (Google Search Console / Яндекс
// Вебмастер) — владелец может вставить либо сам код, либо весь HTML-тег
// целиком (решение пользователя 2026-07-14: "чтобы пользователь не
// мучался... код подтверждения сам извлекался, даже если он вставит
// полностью HTML-тег"). Если строка похожа на <meta ...content="X"...>,
// вытаскиваем значение content= независимо от порядка атрибутов; иначе
// считаем, что это уже голый код, как есть. Используется и клиентом
// (settings/landing/page.tsx, на blur), и сервером (PATCH-роут, источник
// истины — на случай прямого запроса к API в обход формы).
export function extractVerificationCode(input: string): string {
  const trimmed = input.trim();
  const match = trimmed.match(/content\s*=\s*["']([^"']+)["']/i);
  return match ? match[1] : trimmed;
}
